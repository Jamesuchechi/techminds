// Logical And(&&)

let age = 10;
let hasID = true;

if(age >= 18 && hasID){
    console.log("Permission Granted");
}else{
    console.log('permission Denied')
}

// Logical OR (||)

let isTeacher = false;
let isAdmin = false;

if(isTeacher || isAdmin){
    console.log('Access Granted')
}else{
    console.log('access denied, you are not an admin')
}

